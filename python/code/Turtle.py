class Turtle:

    # 属性
    color = 'green'
    weight = 10
    legs = 4
    shell = True
    mouth = '大嘴'

    # 方法
    def climb(self):
        print('我正在很努力的向前爬')
        
    def run(self):
        print('我正在飞快的向前跑')
        
    def bite(self):
        print('我可以咬你哦')
        
    def eat(self):
        print('有的吃 很满足')

    def sleep(self):
        print('困了,睡了,晚安 Zzzz')
